SELECT * FROM COMPANY WHERE SALARY LIKE '_00%';
SELECT * FROM COMPANY WHERE SALARY LIKE '2_%_%';
SELECT * FROM COMPANY WHERE SALARY LIKE '%2';
SELECT * FROM COMPANY WHERE SALARY LIKE '_2%3';
SELECT * FROM COMPANY WHERE SALARY LIKE '2__3';


-- GLOB
/*
WHERE SALARY GLOB '200*'	查找以 200 开头的任意值
WHERE SALARY GLOB '*200*'	查找任意位置包含 200 的任意值
WHERE SALARY GLOB '?00*'	查找第二位和第三位为 00 的任意值
WHERE SALARY GLOB '2??'	查找以 2 开头，且长度为 3 个字符的任意值，例如，它可能匹配 "200"、"2A1"、"2B2" 等值。
WHERE SALARY GLOB '*2'	查找以 2 结尾的任意值
WHERE SALARY GLOB '?2*3'	查找第二位为 2，且以 3 结尾的任意值
WHERE SALARY GLOB '2???3'	查找长度为 5 位数，且以 2 开头以 3 结尾的任意值
*/
SELECT * FROM COMPANY WHERE SALARY GLOB '200*';
SELECT * FROM COMPANY WHERE SALARY GLOB '*200*';
SELECT * FROM COMPANY WHERE SALARY GLOB '?00*';
SELECT * FROM COMPANY WHERE SALARY GLOB '2??';
SELECT * FROM COMPANY WHERE SALARY GLOB '*2';
SELECT * FROM COMPANY WHERE SALARY GLOB '?2*3';
SELECT * FROM COMPANY WHERE SALARY GLOB '2???3';

