my_int = 3
print(type(my_int))

my_int = int(32)
print(type(my_int))

my_float = 32.85
print(type(my_float))

my_float = float(32.85)
print(type(my_float))

my_complex_number = 32+4j
print(type(my_complex_number))

my_complex_number = complex(32+4j)
print(type(my_complex_number))
